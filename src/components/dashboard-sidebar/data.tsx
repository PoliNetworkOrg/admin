const flattenNavigation = (items: NavItem[]): Record<string, string> => {
  const map: Record<string, string> = {}
  const traverse = (list: NavItem[]) => {
    for (const item of list) {
      map[item.url] = item.title
      if (item.items) traverse(item.items)
    }
  }
  traverse(items)
  return map
}

